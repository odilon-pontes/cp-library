/*
Ela é muito usada para:
- encontrar pares
- trabalhar com intervalos
- somas específicas
- remover duplicatas
- sliding window
*/
int twoPointers(int n) {
    int l = 0, r = n-1;

    bool condicao_ruim = true;
    while (l < r) {
        // expande r

        while (condicao_ruim)
            l++;
    }
}